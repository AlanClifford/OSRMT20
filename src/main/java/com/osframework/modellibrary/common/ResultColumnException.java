package com.osframework.modellibrary.common;

public class ResultColumnException extends Exception {

	private static final long serialVersionUID = 1L;
	
	public ResultColumnException() {
		super();
	}

	public ResultColumnException(String message) {
		super(message);
	}

	public ResultColumnException(String message, Throwable cause) {
		super(message, cause);
	}

	public ResultColumnException(Throwable cause) {
		super(cause);
	}

}
